class increment{
	public static void main(String[] args) {
	    int a,b;
		a=20;
		b=20;
		System.out.println(a);
		++a ;                   //a=20+1--->a=21
		System.out.println(a);

		System.out.println();  // just for gap

		System.out.println(b);
		b++;                   // b=20+1 --> b=21
		System.out.println(b);
	}
}