class comparison{
	public static void main(String[] args) 
	{
		int a,b;                        // comparison: comparison operators will compare
		                                // given 2 values returns boolean result
		a=10;b=20;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a!=b);
	}
}